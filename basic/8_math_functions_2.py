import math

pi=math.pi
print(pi)

exponentialConstant=math.e
print(f"Exponential constant is: {exponentialConstant}")

a=625
squareRoot=math.sqrt(625)
print(f"squareRoot of {a} is: {squareRoot}")



#ceil() will round of value to next value always
a=3.1234
result=math.ceil(a)
print(f"ceil of {a} is: {result}")

#floor() will round of value to previous value always
a=3.999999
result=math.floor(a)
print(f"ceil of {a} is: {result}")